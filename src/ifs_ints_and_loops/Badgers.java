package ifs_ints_and_loops;

public class Badgers {
	public static void main(String[] args) {

		for (int b = 0; b < 3; b++) {

			for (int i = 0; i < 12; i++) {
				System.out.println("Badgers");
			}

			for (int k = 0; k < 3; k++) {
				System.out.println("Mushroom");
			}

		}
		for (int s = 0; s < 3; s++) {
			System.out.println("snake");
		}

	}
}
